async def generate(message: Message):
    keybord = InlineKeyboardMarkup(inline_keyboard=[
        [InlineKeyboardButton(text="–°–æ—Ö—Ä–∞–Ω–∏—Ç—å –ø–∞—Ä–æ–ª—å", callback_data="save")]
    ])

    char = ""
    char+=string.ascii_letters
    char+=string.digits
    char+=string.punctuation

    password = []

    for el in range(14):
        letter = random.choice(char)
        password.append(letter)
    
    await message.answer("–í–∞—à —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –ø–∞—Ä–æ–ª—å: " + "".join(password), reply_markup=keybord)


